#!/usr/bin/env python3

def cat_arrays(arr1, arr2):
    """
    Concatène deux tableaux.

    Arguments:
        arr1 (list): Le premier tableau.
        arr2 (list): Le deuxième tableau.

    Return:
        list: Un nouveau tableau qui est la concaténation de arr1 et arr2.
    """
    return arr1 + arr2
